package org.magic.draft.api.scryfall.cardSearchParts;

public class ImageUris {
    private String small;
    private String normal;
    private String large;
    private String png;
    private String artCrop;
    private String borderCrop;

    public String getSmall() { return small; }
    public void setSmall(String value) { this.small = value; }

    public String getNormal() { return normal; }
    public void setNormal(String value) { this.normal = value; }

    public String getLarge() { return large; }
    public void setLarge(String value) { this.large = value; }

    public String getPng() { return png; }
    public void setPng(String value) { this.png = value; }

    public String getArtCrop() { return artCrop; }
    public void setArtCrop(String value) { this.artCrop = value; }

    public String getBorderCrop() { return borderCrop; }
    public void setBorderCrop(String value) { this.borderCrop = value; }
}
